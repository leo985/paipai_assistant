package com.dianwang.paipai.robot.support;


public class CoordBean {
	
	private int x;
	private int y;
	
	/**
	 * 获取x坐标
	 * @return x坐标
	 */
	public int getX() {
		return x;
	}
	public void setX(int x) {
		this.x = x;
	}
	
	/**
	 * 获取y坐标
	 * @return y坐标
	 */
	public int getY() {
		return y;
	}
	public void setY(int y) {
		this.y = y;
	}
}
